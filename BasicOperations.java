package BasicOperations;

public class BasicOperations {
    public static void main(String [] args){
        int a = 15;
        int b = 5;
        int sum = a+b;
        int diff = a-b;
        int prod = a*b;
        System.out.println("Addition:"+sum);
        System.out.println("Difference:"+diff);
        System.out.println("Product:"+prod);
    }
}
